#------------------------------------------------------------------------------------------------------------
#
#	スレッド情報完全更新
#	-------------------------------------------------------------------------------------
#	@param	$SYS	MELKOR
#	@return	なし
#
#------------------------------------------------------------------------------------------------------------
sub UpdateAll
{
	my $this = shift;
	my ($SYS) = @_;
	my (@dirSet, $id, $base, $n, $num, $first, $subj, $el, $psort, %idhash);
	
	undef $this->{'SUBJECT'};
	undef $this->{'RES'};
	$psort = $this->{'SORT'};
	$this->{'SORT'} = [];
	%idhash = ();
	@dirSet = ();
	
	$base	= $SYS->Get('BBSPATH') . '/' . $SYS->Get('BBS') . '/dat';
	$num	= 0;
	
	# ディレクトリ内一覧を取得
	if (opendir(my $f_datdir, $base)) {
		@dirSet = readdir($f_datdir);
		closedir($f_datdir);
	}
	
	foreach $el	(@dirSet) {
		if ($el =~ /(.*)\.dat/ && open(my $f_dat, '<', "$base/$el")) {
			flock($f_dat, 2);
			$n	= 0;
			$id	= $1;
			while (<$f_dat>) {
				if ($n == 0) {
					chomp $_;
					$first = $_;
				}
				$n++;
			}
			close($f_dat);
			(undef, undef, undef, undef, $subj) = split(/<>/, $first);
			$this->{'SUBJECT'}->{$id}	= $subj;
			$this->{'RES'}->{$id}		= $n;
			$idhash{$id} = 1;
			$num++;
		}
	}
	$this->{'NUM'} = $num;
	
	foreach $id (@$psort) {
		if (defined $idhash{$id}) {
			push @{$this->{'SORT'}}, $id;
			delete $idhash{$id};
		}
	}
	foreach $id (sort keys %idhash) {
		unshift @{$this->{'SORT'}}, $id;
	}
}

