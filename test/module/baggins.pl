{
	my $this = shift;
	my ($id) = @_;
	my ($iid, $n);
	
	$n = 0;
	foreach $iid (@{$this->{'SORT'}}) {
		if ($iid eq $id) {
			return $n;
		}
		$n++;
	}
	return -1;
}


