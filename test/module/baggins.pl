sub new
{
	my $this = shift;
	my ($obj, %SUBJECT, %RES, @SORT);
	
	$obj = {
		'SUBJECT'	=> \%SUBJECT,
		'RES'		=> \%RES,
		'SORT'		=> \@SORT,
		'NUM'		=> 0
	};
	bless $obj, $this;
	
	return $obj;
}

#------------------------------------------------------------------------------------------------------------
#
#	スレッド情報読み込み
#	-------------------------------------------------------------------------------------
#	@param	$Sys	MELKOR
#	@return	なし
#
#------------------------------------------------------------------------------------------------------------
sub Load
{
	my $this = shift;
	my ($Sys) = @_;
	my ($path, @elem, $num);
	
	undef($this->{'SUBJECT'});
	undef($this->{'RES'});
	undef($this->{'SORT'});
	
	$path	= $Sys->Get('BBSPATH') . '/' .$Sys->Get('BBS') . '/subject.txt';
	$num	= 0;
	
	if (open(my $f_subj, '<', $path)) {
		flock($f_subj, 2);
		while (<$f_subj>) {
			next if ($_ !~ /<>/);
			@elem = split(/<>/, $_);
			($elem[0], undef) = split(/\./, $elem[0]);
			$elem[1] =~ s/ ?\((\d+)\)\n//;
			$elem[2] = $1;
			
			$this->{'SUBJECT'}->{$elem[0]}	= $elem[1];
			$this->{'RES'}->{$elem[0]}		= $elem[2];
			push @{$this->{'SORT'}}, $elem[0];
			$num++;
		}
		close($f_subj);
		$this->{'NUM'} = $num;
	}
}

#------------------------------------------------------------------------------------------------------------
#
#	スレッド情報保存
#	-------------------------------------------------------------------------------------
#	@param	$Sys	MELKOR
#	@return	なし
#
#------------------------------------------------------------------------------------------------------------
sub Save
{
	my $this = shift;
	my ($Sys) = @_;
	my ($path, $data);
	
	$path = $Sys->Get('BBSPATH') . '/' . $Sys->Get('BBS') . '/subject.txt';
	
	if (open(my $f_subj, (-f $path ? '+<' : '>'), $path)) {
		flock($f_subj, 2);
		seek($f_subj, 0, 0);
		binmode($f_subj);
		
		foreach (@{$this->{'SORT'}}) {
			next if (! defined $this->{'SUBJECT'}->{$_});
			$data = "$_.dat<>" . $this->{'SUBJECT'}->{$_} . ' (' . $this->{'RES'}->{$_} . ')';
			
			print $f_subj "$data\n";
		}
		
		truncate($f_subj, tell($f_subj));
		close($f_subj);
		chmod $Sys->Get('PM-TXT'), $path;
	}
}

#------------------------------------------------------------------------------------------------------------
#
#	オンデマンド式レス数更新
#	-------------------------------------------------------------------------------------
#	@param	$Sys	MELKOR
#	@param	$id	スレッドID
#	@param	$val	レス数
#	@param	$age	ageるなら1
#	@return	なし
#
#------------------------------------------------------------------------------------------------------------
sub OnDemand
{
	my $this = shift;
	my ($Sys, $id, $val, $age) = @_;
	my ($path, @elem, $num, $data);
	
	undef($this->{'SUBJECT'});
	undef($this->{'RES'});
	undef($this->{'SORT'});
	
	$path	= $Sys->Get('BBSPATH') . '/' .$Sys->Get('BBS') . '/subject.txt';
	$num	= 0;
	
	if (open(my $f_subj, (-f $path ? '+<' : '>'), $path)) {
		flock($f_subj, 2);
		seek($f_subj, 0, 0);
		
		# subject読み込み
		while (<$f_subj>) {
			next if ($_ !~ /<>/);
			@elem = split(/<>/, $_);
			($elem[0], undef) = split(/\./, $elem[0]);
			$elem[1] =~ s/ ?\((\d+)\)\n//;
			$elem[2] = $1;
			
			$this->{'SUBJECT'}->{$elem[0]} = $elem[1];
			$this->{'RES'}->{$elem[0]} = $elem[2];
			push @{$this->{'SORT'}}, $elem[0];
			$num++;
		}
		$this->{'NUM'} = $num;

		# レス数更新
		if ( exists($this->{'RES'}->{$id}) ) {
			$this->{'RES'}->{$id} = $val;
		}

		if ( $age eq 1 ) {
			$num = 0;
			foreach my $lid (@{$this->{'SORT'}}) {
				if ($id eq $lid) {
					splice @{$this->{'SORT'}}, $num, 1;
					unshift @{$this->{'SORT'}}, $lid;
					last;
				}
				$num++;
			}
		}

		# subject書き込み
		seek($f_subj, 0, 0);
		binmode($f_subj);
		
		foreach (@{$this->{'SORT'}}) {
			next if (! defined $this->{'SUBJECT'}->{$_});
			$data = "$_.dat<>" . $this->{'SUBJECT'}->{$_} . ' (' . $this->{'RES'}->{$_} . ')';
			
			print $f_subj "$data\n";
		}
		
		truncate($f_subj, tell($f_subj));
		close($f_subj);
		chmod $Sys->Get('PM-TXT'), $path;
	}

}

#------------------------------------------------------------------------------------------------------------
#
#	スレッドIDセット取得
#	-------------------------------------------------------------------------------------
#	@param	$kind	検索種別('ALL'の場合すべて)
#	@param	$name	検索ワード
#	@param	$pBuf	IDセット格納バッファ
#	@return	キーセット数
#
#------------------------------------------------------------------------------------------------------------
sub GetKeySet
{
	my $this = shift;
	my ($kind, $name, $pBuf) = @_;
	my ($key, $n);
	
	$n = 0;
	
	if ($kind eq 'ALL') {
		foreach $key (@{$this->{SORT}}) {
			push @$pBuf, $key;
			$n++;
		}
	}
	else {
		foreach $key (keys %{$this->{$kind}}) {
			if ($this->{$kind}->{$key} eq $name || $kind eq 'ALL') {
				push @$pBuf, $key;
				$n++;
			}
		}
	}
	
	return $n;
}

